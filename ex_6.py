a = float(input('Результат первого дня в км: '))
b = int(input('Нужный результат в км: '))
i = 1

print('{}-й день: {}'.format(i, a))
while a < b:
    a += 0.1 * a
    i = i + 1
    print('{}-й день: {}'.format(i, a))

print('Ответ: на {}-й день спортсмен достиг результата — не менее {} км.'.format(i, b))
