a = float(input('Выручка = '))
b = float(input('Издержки = '))

if a > b:
    c = a - b
    print('Результат компании: прибыль (', c, ')')
    print('Рентабильность выручки: ', c / a)
    d = float(input('Численность сотрудников: '))
    print('Прибыль одного сотрудника: ', c / d)
elif a == b:
    print('Результат компании: выручка покрыла издержки')
else:
    print('Результат компании: убыток')
